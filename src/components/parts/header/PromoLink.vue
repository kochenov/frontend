    textLink: "Разработчик сайта",
    textLinkOn: " Коченов Дмитрий",
  },
];
const increment = () => {
  num.value = links.length - 1 === num.value ? 0 : ++num.value;
};
const discrement = () => {
  num.value = num.value === 0 ? links.length - 1 : --num.value;
};
</script>

