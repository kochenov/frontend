    <button @click="discrement" class="promo-info-link__btn-left">
      <fa icon="angle-left" />
    </button>
    <ul class="promo-info-link__items">
      <li class="promo-info-link__item">
        <a class="promo-info-link__link" :href="links[num].url">
          {{ links[num].textLink }}
